#include "../minishell.h"

int	ft_skip_quates(int *i, char *line)
{
	if (line[*i] == '"')
	{
		(*i)++;
		while (line[*i] != '\0' && line[*i] != '"')
			(*i)++;
		if (line[*i] == '"')
			(*i)++;
	}
	if (line[*i] == '\'')
	{
		(*i)++;
		while (line[*i] != '\0' && line[*i] != '\'')
			(*i)++;
		if (line[*i] == '\'')
			(*i)++;
	}
	return (*i);
}
