#include "../minishell.h"

bool	ft_strstr(char *line, char *sp)
{
	while (*sp != '\0' && *line != '\0' && *sp == *line)
	{
		sp++;
		line++;
	}
	if (*sp == '\0')
		return (true);
	return (false);
}
